namespace WoWFormatLib.Utils
{
    public struct BGRAColor
    {
        public char B;
        public char G;
        public char R;
        public char A;

        public BGRAColor(char b, char g, char r, char a)
            : this()
        {
            B = b;
            G = g;
            R = r;
            A = a;
        }
    }
}